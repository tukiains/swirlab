function mass = molar_mass(molecule)

switch molecule
  case 'no2'
    mass = 46.00558;
  case 'o3'
    mass = 47.99829;
  case 'ch4'
    mass = 16.04257;
  case 'co2'
    mass = 44.00964;
  case 'co'
    mass = 28.01021;
  case 'n2o'
    mass = 44.01287;
  case 'no'
    mass = 30.00615;
  case 'h2o'
    mass = 18.01532;
  case 'o2'
    mass = 31.99886;
  case 'hdo'
    mass = 19.02148;
  case 'hf'
    mass = 20.01;
end
